    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        String nama = txtNama.getText();
        String meja = txtMeja.getText();
        int confirm = JOptionPane.showConfirmDialog(this, "Pelanggan bernama"+nama+" dengan meja no "+meja+", pesanan anda selesai!", "Konfirmasi",JOptionPane.OK_CANCEL_OPTION);
        
        if(confirm==JOptionPane.OK_OPTION){
            txtNama.setText("");
            txtMeja.setText("");
        }
    }//GEN-LAST:event_btnSelesaiActionPerformed

    private void btnKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKeluarActionPerformed
        int pil = JOptionPane.showConfirmDialog(this,"Apakah yakin ingin keluar?","Peringatan",JOptionPane.YES_NO_OPTION);
        if(pil==JOptionPane.YES_OPTION){
            System.exit(0);
        }
    }//GEN-LAST:event_btnKeluarActionPerformed

