    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        double makan = Double.parseDouble(txtMakanan.getText());
        double minum = Double.parseDouble(txtMinuman.getText());
        double total = makan+minum;
        txtHarga.setText(Double.toString(total));
        
        double pajak = (0.1)*total;
        txtPajak.setText(Double.toString(pajak));
        
        double bayar = total+pajak;
        txtTotal.setText(Double.toString(bayar));
    }//GEN-LAST:event_btnProsesActionPerformed

