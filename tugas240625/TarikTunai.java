    private void btnTarikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTarikActionPerformed
        try {
            String jmlTariktrk = txtTarik.getText().trim();
            if (jmlTariktrk.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Jumlah penarikan tidak boleh kosong.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int jmlTarik = Integer.parseInt(jmlTariktrk);
            if (jmlTarik <= 50000) {
                JOptionPane.showMessageDialog(this, "Jumlah Penarikan harus lebih dari Rp. 50000.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (jmlTarik > Menu.saldo) {
                JOptionPane.showMessageDialog(this, "Saldo Anda tidak mencukupi untuk melakukan Penarikan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Menu.saldo -= jmlTarik;
            JOptionPane.showMessageDialog(this, "Tarik tunai berhasil!\nSilahkan ambil uang anda", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            txtTarik.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harap masukkan angka yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
            txtTarik.setText("");
        }
    }//GEN-LAST:event_btnTarikActionPerformed

