    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        double total = Double.parseDouble(txtTotal.getText());
        double bayar = Double.parseDouble(txtBayar.getText());
        if (bayar >= total) {
            double kembali = bayar - total;
            txtKembali.setText(Double.toString(kembali));
            JOptionPane.showMessageDialog(this, "Terima kasih telah menggunakan jasa kami\nKembalian anda Rp."+txtKembali.getText(), "Pelanggan Yth.", JOptionPane.INFORMATION_MESSAGE );
        } else {
            JOptionPane.showMessageDialog(this, "Uang anda tidak cukup", "Peringatan", JOptionPane.WARNING_MESSAGE);
            txtBayar.setText("");
        }
    }//GEN-LAST:event_btnBayarActionPerformed

