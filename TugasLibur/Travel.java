    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        try {
            int kelas = cbKelas.getSelectedIndex();
            if (kelas == 0) {
                JOptionPane.showMessageDialog(this, "Silakan pilih kelas terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (txtTiket.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silakan isi jumlah tiket!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int jmlTiket = Integer.parseInt(txtTiket.getText());
            double harga = 0;

            switch (kelas) {
                case 1:
                    harga = 200000;
                    break;
                case 2:
                    harga = 300000;
                    break;
                case 3:
                    harga = 550000;
                    break;
            }

            double total = jmlTiket * harga;
            double diskon = 0;

            if (jmlTiket > 3) {
                diskon = total * 0.1;
            }

            double totalBayar = total - diskon;

            txtDiskon.setText(String.valueOf(diskon));
            txtTotal.setText(String.valueOf(totalBayar));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah tiket harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnProsesActionPerformed

